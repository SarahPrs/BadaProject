package com.bada_project.model;

public class Composante {
	private int code;
	private String date_modification;
	private String nom;
	private String alias;
	private int type_de_composante;
	private int couleur;
	private int couleur_police;
	private int code_couleur;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getDate_modification() {
		return date_modification;
	}
	public void setDate_modification(String date_modification) {
		this.date_modification = date_modification;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public int getType_de_composante() {
		return type_de_composante;
	}
	public void setType_de_composante(int type_de_composante) {
		this.type_de_composante = type_de_composante;
	}
	public int getCouleur() {
		return couleur;
	}
	public void setCouleur(int couleur) {
		this.couleur = couleur;
	}
	public int getCouleur_police() {
		return couleur_police;
	}
	public void setCouleur_police(int couleur_police) {
		this.couleur_police = couleur_police;
	}
	public int getCode_couleur() {
		return code_couleur;
	}
	public void setCode_couleur(int code_couleur) {
		this.code_couleur = code_couleur;
	}
	
	

}
