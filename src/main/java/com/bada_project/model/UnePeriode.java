package com.bada_project.model;

import java.util.Date;

public class UnePeriode {
	private Date JourDebut;
	private Date JourFin;
	
	public Date getJourDebut() {
		return JourDebut;
	}
	public void setJourDebut(Date jourDebut) {
		JourDebut = jourDebut;
	}
	public Date getJourFin() {
		return JourFin;
	}
	public void setJourFin(Date jourFin) {
		JourFin = jourFin;
	}
	
	

}
